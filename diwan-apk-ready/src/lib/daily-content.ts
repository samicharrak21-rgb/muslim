export const getDailyAyah = (date: Date = new Date()): DailyAyah => {
  const dayOfYear = Math.floor(
    (date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86_400_000
  );
  return DAILY_AYAHS[dayOfYear % DAILY_AYAHS.length];
};

export const getWeeklyHadith = (date: Date = new Date()): WeeklyHadith => {
  // رقم الأسبوع في السنة
  const start = new Date(date.getFullYear(), 0, 1);
  const week = Math.floor(((date.getTime() - start.getTime()) / 86_400_000 + start.getDay() + 1) / 7);
  return WEEKLY_HADITHS[week % WEEKLY_HADITHS.length];
};
